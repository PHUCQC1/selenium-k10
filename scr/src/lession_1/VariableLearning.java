package lession_1;

public class VariableLearning {
    public static void main(String[] args) {
        // snake_ case, camelCase ( hay dung trong java)
        // 8 loai data type:
        // so nguyen byte, short, int, long
        int myNum = 18;
        long myNumber = 18L;
        // so thap phan
        float myFloatNumber = 3.14f;
        double myDoubleNumber = 3.14D;
        //kieu data type
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);

        System.out.println(Short.MAX_VALUE);
        System.out.println(Integer.MAX_VALUE);
        //MY_CONSTANT (so khong thay doi)
        final int MY_CONSTANT = 123;
        //MY_CONSTANT = 234; // fix cung roi nen doi kieu du lieu se bao loi




    }
}

